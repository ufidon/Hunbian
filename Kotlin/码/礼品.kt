package 礼品
import java.awt.Color

fun 买瓷蛙(){
  // 用默认构函创建对象
  val 红瓷蛙 = 瓷蛙()

  // 使用对象的方法
  红瓷蛙.瓷蛙描绘()

  // 修改对象的属性
  红瓷蛙.重量 = 500.0
  红瓷蛙.价格 = 123.99

  println(红瓷蛙)

  // 用主构函法1造对象
  val 黑瓷蛙 = 瓷蛙(颜色 = Color.BLACK)
  黑瓷蛙.瓷蛙描绘()

  // 用主构函法2造对象
  val 蓝瓷蛙 = 瓷蛙(颜色 = Color.BLUE, 价格 = 100.99)
  蓝瓷蛙.瓷蛙描绘()

  // 用次构函造对象
  val 黄瓷蛙 = 瓷蛙(高度 = 2.3)
  黄瓷蛙.瓷蛙描绘()
}

fun main(程参: Array<String>){
  买瓷蛙()

  val 金瓷蛙 =瓷蛙(价格 = 2999.99)
  金瓷蛙.快递保费()
